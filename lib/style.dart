Color getColorFromWarnaEartag(String warna) {
  switch (warna.toLowerCase()) {
    case 'merah':
      return Colors.red;
    case 'kuning':
      return Colors.yellow.shade700;
    case 'hijau':
      return Colors.green;
    case 'putih':
      return Colors.white;
    case 'hitam':
      return Colors.black;
    case 'orange':
      return Colors.orange;
    case 'ungu':
      return Colors.purple;
    case 'biru':
      return Colors.blue;
    case 'coklat':
      return Colors.brown;
    default:
      return Colors.grey.shade400;
  }
}

Color getStatusColor(String status) {
  switch (status) {
    case "Sakit":
      return Colors.orange;
    case "Mortalitas":
      return Colors.red;
    case "Sehat":
      return Colors.green;
    default:
      return Colors.grey;
  }
}

IconData getStatusIcon(String status) {
  switch (status) {
    case "Sakit":
      return Icons.warning_amber;
    case "Mortalitas":
      return Icons.close;
    case "Sehat":
      return Icons.check_circle;
    default:
      return Icons.error;
  }
}

bool isDarkColor(Color color) {
  return color.computeLuminance() < 0.5;
}

Color getColorFromName(String colorName) {
  switch (colorName) {
    case 'Merah':
      return Colors.red;
    case 'Kuning':
      return Colors.yellow;
    case 'Hijau':
      return Colors.green;
    case 'Orange':
      return Colors.orange;
    case 'Putih':
      return Colors.white;
    case 'Hitam':
      return Colors.black;
    case 'Biru':
      return Colors.blue;
    default:
      return Colors.grey;
  }
}
